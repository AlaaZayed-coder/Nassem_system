const suggestions = [
  ['باب', 'أبواب ومستلزماتها'],
  ['رول', 'أبواب رول ومستلزماتها'],
  ['موتور', 'مواتير وماكينات وقطع كهربائية'],
  ['ماكينة', 'مواتير وماكينات وقطع كهربائية'],
  ['روزيت', 'روزيت وغطاء وقطع تشطيب'],
  ['صاج', 'صاج ومواد خام'],
  ['مفصلة', 'مفصلات وأقفال وقطع تثبيت'],
  ['قفل', 'مفصلات وأقفال وقطع تثبيت'],
  ['شبك', 'شبك وسلك ومجاري'],
  ['زينة', 'زينة حديد وحدادة ديكورية'],
  ['دهان', 'خدمات وتصنيع وتشطيب'],
  ['قص', 'خدمات وتصنيع وتشطيب'],
  ['ثني', 'خدمات وتصنيع وتشطيب']
];

export function suggestCategory(name = '') {
  const text = String(name);
  const hit = suggestions.find(([keyword]) => text.includes(keyword));
  return hit ? hit[1] : '';
}
